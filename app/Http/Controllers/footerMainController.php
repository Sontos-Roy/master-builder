    {
        $data = $request->validate([
            'description' => 'required',
            'phone1' => 'required',
            'phone2' => '',
            'email' => 'email',
            'logo' => 'image'
        ]);

        $create = Footer::create($data);

        createImage($create, "images/footer", 'logo', $request, 'footer_');

        return redirect()->route("footer.index")->with("success", "Data Created Successfully");
